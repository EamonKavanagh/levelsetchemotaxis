function length = compute_iso_arc_length(nodes, s0, s1)

x = nodes(:,1); y = nodes(:,2);

%Gauss points for arc length integral (10 pt).

w1=0.2955242247147529; p1=	-0.1488743389816312;
w2=0.2955242247147529; p2=	0.1488743389816312;
w3=0.2692667193099963; p3=	-0.4333953941292472;
w4=0.2692667193099963; p4=	0.4333953941292472;
w5=0.2190863625159820; p5=	-0.6794095682990244;
w6=0.2190863625159820; p6=	0.6794095682990244;
w7=0.1494513491505806; p7=	-0.8650633666889845;
w8=0.1494513491505806; p8=	0.8650633666889845;
w9=0.0666713443086881; p9=	-0.9739065285171717;
w0=0.0666713443086881; p0=	0.9739065285171717;

gauss = (s1-s0)*[p1 p2 p3 p4 p5 p6 p7 p8 p9 p0]/2 + (s0+s1)/2;  
weights = [w1;w2;w3;w4;w5;w6;w7;w8;w9;w0];

g21 = x(2)*(4*gauss-1); g22 = y(2)*(4*gauss-1);
g31 = x(3)*(4*gauss-3); g32 = y(3)*(4*gauss-3);
g41 = x(4)*(4-8*gauss); g42 = y(4)*(4-8*gauss);

length = abs((s1-s0)*sqrt((g21+g31+g41).^2 + (g22+g32+g42).^2)*weights/2);